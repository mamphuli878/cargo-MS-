    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int selectedRow = tblStudent.getSelectedRow();
        if (selectedRow != -1) {
            String cargoId = txtfldCargoId.getText();
            String shippingCost = txtfldShippingCost.getText();
            String weight = txtWeight.getText();
            String volume = txtfldVolume.getText();
            String origin = txtfldOrigin.getText();
            String destination = txtfldDestination.getText();
            String status = cmbStatus.getSelectedItem().toString();
            String estimatedDelivery = txtfldEstimatedDelivery.getText();

            if (ValidationUtil.isValidCargoId(cargoId, lblError) &&
                ValidationUtil.isValidNumericValue(shippingCost, lblError) &&
                ValidationUtil.isValidNumericValue(weight, lblError) &&
                ValidationUtil.isValidNumericValue(volume, lblError) &&
                ValidationUtil.isValidLocation(origin, lblError) &&
                ValidationUtil.isValidLocation(destination, lblError) &&
                ValidationUtil.isValidStatus(status, lblError) &&
                ValidationUtil.isValidEstimatedDelivery(estimatedDelivery, lblError)) {

                // Update cargo data in the table
                DefaultTableModel model = (DefaultTableModel) tblStudent.getModel();
                model.setValueAt(cargoId, selectedRow, 0);
                model.setValueAt(shippingCost, selectedRow, 1);
                model.setValueAt(weight, selectedRow, 2);
                model.setValueAt(volume, selectedRow, 3);
                model.setValueAt(origin, selectedRow, 4);
                model.setValueAt(destination, selectedRow, 5);
                model.setValueAt(status, selectedRow, 6);
                model.setValueAt(estimatedDelivery, selectedRow, 7);
                JOptionPane.showMessageDialog(this, "Cargo data updated successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

