    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        String cargoId = txtfldCargoId.getText(); 
        String shippingCost = txtfldShippingCost.getText(); 
        String weight = txtWeight.getText(); 
        String volume = txtfldVolume.getText(); 
        String origin = txtfldOrigin.getText(); 
        String destination = txtfldDestination.getText(); 
        String status = cmbStatus.getSelectedItem().toString(); 
        String estimatedDelivery = txtfldEstimatedDelivery.getText(); 
        
        // Validate the inputs using the ValidationUtil methods
        if (ValidationUtil.isValidCargoId(cargoId, lblError) && 
            ValidationUtil.isValidNumericValue(shippingCost, lblError) &&
            ValidationUtil.isValidNumericValue(weight, lblError) &&
            ValidationUtil.isValidNumericValue(volume, lblError) &&
            ValidationUtil.isValidLocation(origin, lblError) &&
            ValidationUtil.isValidLocation(destination, lblError) &&
            ValidationUtil.isValidStatus(status, lblError) &&
            ValidationUtil.isValidEstimatedDelivery(estimatedDelivery, lblError)) {
            
            int cargoIdValue = Integer.parseInt(cargoId); 
            double shippingCostValue = Double.parseDouble(shippingCost); 
            double weightValue = Double.parseDouble(weight); 
            double volumeValue = Double.parseDouble(volume); 
            int estimatedDeliveryValue = Integer.parseInt(estimatedDelivery); 

            CargoModel cargo = new CargoModel(cargoIdValue, shippingCostValue, weightValue, volumeValue, 
                                              origin, destination, status, estimatedDeliveryValue);

            registerStudent(cargo);

            JOptionPane.showMessageDialog(this, "Cargo data has been successfully added.", "Success", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Please enter valid data for all fields.", "Validation Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed

        int cargoIdToRemove = Integer.parseInt(txtfldCargoId.getText());
        DefaultTableModel model = (DefaultTableModel) tblStudent.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            if (model.getValueAt(i, 0).equals(cargoIdToRemove)) {
                model.removeRow(i);
                break;
            }
        }
    
    }//GEN-LAST:event_btnRemoveActionPerformed

